// Script pour convertir Excel → Visual World JSON
function excelToVisualWorld(excelData) {
    const objects = excelData.map((item, index) => ({
        _id: `position_${index + 1}`,
        objectId: `object_${index + 1}`,
        componentId: "681bd1c4fe6d77d26c416b9a", // K - Work Item
        object: {
            data: {
                name: item.name,
                due_iteration: item.due_iteration,
                duration: item.duration,
                fte: item.fte,
                iteration: item.iteration,
                job: item.job,
                phase: item.phase,
                team: item.team,
                bvalue: item.bvalue,
                start_date: item.start_date
            },
            protect: {
                styleBackgroundColor: item["couleur de fond"] || "",
                styleOutlineColor: item["couleur bordures"] || "",
                styleColor: item["couleur du texte"] || ""
            }
        },
        position: {
            data: {
                left: parseInt(item.x),
                top: parseInt(item.y),
                width: parseInt(item.l),
                height: parseInt(item.h),
                zIndex: parseInt(item["z-index"]),
                rotation: parseInt(item.rotation) || 0
            },
            protect: {
                isBackground: item.plan === "background"
            }
        }
    }));

    return {
        VW: {
            objects,
            components: [],
            linkModels: [],
            links: [],
            boardComponents: []
        },
        options: {
            importForeground: true,
            importBackground: false,
            importLinks: true,
            flushForeground: false,
            flushBackground: false,
            updateModel: false,
            useNewObjectId: true
        }
    };
}

